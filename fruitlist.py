fruits = []
vegetables = []

# .append adds stuff to your list

fruits.append("Banana")
fruits.append("Apple")
fruits.append("Grape")
fruits.append("Mango")

print(fruits)


# .remove removes things from the list

fruits.remove("Mango")

print(fruits)


vegetables.append("Carrot")
vegetables.append("Squash")

print(vegetables)

# this is how you combined lists together
produce = fruits + vegetables


print(produce)